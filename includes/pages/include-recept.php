  <h1>Recept</h1>
  <div class="hr-small-underline"></div>
  <h3 class="lead">Jordgubbsrecept från Stenatorp.</h3>


  <h3>Rårörd jordgubbssaft</h3>
  <div class="hr-small"></div>

  <ul>
    <li>6 liter jordgubbar</li>
    <li>50 g citronsyra</li>
    <li>3 ½ liter vatten</li>
    <li>1 kg socker per silad liter saft</li>
  </ul>

  <p class="recept-text">
    Koka upp vattnet, rör i citronsyran.
    Häll det kokande vattnet över de sköljda och rensade bären.
    Lägg på ett tätt lock och låt det stå kallt 1 dygn.
    Låt saften självrinna genom silduk. Mät saften.
    Rör ner sockret i saften och rör tills det är helt löst.
    Häll saften på rena flaskor och förslut genast.
    Förvara saften kallt. (Går bra att frysa.)
  </p>

  <div class="hr-big"></div>

  <h3>Jordgubb- och flädersaft</h3>
  <div class="hr-small"></div>

  <ul>
    <li>25 st fläderblommor</li>
    <li>2 liter jordgubbar</li>
    <li>1 citron</li>
    <li>2 kg socker</li>
    <li>30 g citronsyra</li>
    <li>2 krm natriumbensoat</li>
    <li>2 liter vatten</li>
  </ul>

  <p class="recept-text">
    Skölj fläderblommor, citron o. jordgubbar
    Jordgubbarna mosas.
    Citronen skärs i skivor.
    Lägg alla ingredienserna i en rostfri bunke och slå över det kokande vattnet.
    Låt stå i 5-6 dygn. Rör om då och då.
    Sila saften och frys den.
  </p>

  <div class="hr-big"></div>

  <h3>Jordgubbsmarmelad</h3>
  <div class="hr-small"></div>

  <ul>
    <li>1 kg jordgubbar</li>
    <li>¾ dl vatten</li>
    <li>1,3 kg socker</li>
    <li>1 tsk citronsyra</li>
    <li>1 flaska certo fruktpektin</li>
  </ul>

  <p class="recept-text">
    Skölj och rensa bären. Mosa dem, gärna i matberedare.
    Koka upp bärmassa och vatten. Sjud 5 min.under lock.
    Tillsätt socker och citronsyra.
    Koka upp och låt koka kraftigt 1 min. under omrörning.
    Ta kastrullen från värmen och rör i fruktpektin.
    Skumma väl och rör i 5 min. så att massan svalnar och bären ej flyter upp.
    Fyll marmeladen på väl rengjorda burkar och sätt på lock.
    Förvara svalt.
  </p>

  <div class="hr-big"></div>

  <h3>Jordgubbskräm</h3>
  <div class="hr-small"></div>


  <ul>
    <li>5 – 7,5 dl jordgubbar</li>
    <li>5 dl vatten  ( hälften med frusna bär )</li>
    <li>¾ - 1 dl socker</li>
    <li>2,5 msk potatismjöl</li>
  </ul>

  <p class="recept-text">
    Dela jordgubbarna.
    Kokas upp under försiktig omrörning.
    Blanda allt.
    Häll upp.
    Sockra på ytan så det ej skinnar sig.
  </p>

  <div class="hr-big"></div>

  <h3>Rårörd jordgubbssylt</h3>
  <div class="hr-small"></div>

  <ul>
    <li>3 kg bär</li>
    <li>½ kg strösocker</li>
    <li>1 påse fryspulver</li>
  </ul>

  <p class="recept-text">
    Skölj och rensa bären.
    Mosa bären i matberedare.
    Blanda ½ kg strösocker med 1 påse fryspulver.
    Rör ner sockerblandningen i sylten. Rör om ordentligt.
    Frys in i lagom stora burkar eller plastpåsar.
    Lätt att ta upp och använda till frukostflingor, pannkakor,
    tårtmellanlägg eller värmas till vaniljglassen.
  </p>

  <div class="hr-big"></div>

  <h3>Jordgubbar med limesocker</h3>
  <div class="hr-small"></div>

  <ul>
    <li>1 liter jordgubbar</li>
    <li>2 st lime</li>
    <li>1 dl strösocker</li>
  </ul>

  <p class="recept-text">
    Lägg upp fina jordgubbar, hela eller delade, gärna med det gröna kvar.
    Tvätta och torka limefrukterna. Riv limeskalet fint och blanda med socker och lite av saften.
    Strö limesockret över jordgubbarna och servera.
  </p>

  <div class="hr-big"></div>

  <h2>Småtips</h2>
  <div class="hr-small-underline"></div>
  <ul>
   <li>Skiva 1 liter jordgubbar och blanda med ½ dl socker.
       Frys in.
       Ta upp och använd jordgubbarna till efterrätt eller
       jordgubbskräm.</li>

    <li>Om jordgubbarna måste sköljas, snoppa då bären efter
        sköljningen, annars kommer vatten in i jordgubben
        och den blir lätt mosig.</li>

    <li>Bästa metoden för korttidsförvaring av bär är att ha
        dem orensade och osköljda i kylskåp.</li>
  </ul>

